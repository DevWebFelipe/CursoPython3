# criei esse arquivo pra colocar exemplos mais simpples

atribuicao_adicao = 1
atribuicao_adicao += 1 # 1 + 1 = 2
print(f'{atribuicao_adicao = }')
print()

atribuicao_subtracao = 5
atribuicao_subtracao -= 2 # 5 - 2 = 3
print(f'{atribuicao_subtracao = }')
print()

atribuicao_multiplicao = 2
atribuicao_multiplicao *= 10 # 2 * 10 = 20
print(f'{atribuicao_multiplicao = }')
print()

atribuicao_potenciacao = 4
atribuicao_potenciacao **= 3 # 4 * 4 * 4 = 64
print(f'{atribuicao_potenciacao = }')
print()

atribuicao_divisao = 20
atribuicao_divisao /= 5 # 20 / 5 = 4
print(f'{atribuicao_divisao = }')
print()

atribuicao_divisao_inteira = 30
atribuicao_divisao_inteira //= 7 # 30 / 7 = 4 (arredondando)
print(f'{atribuicao_divisao_inteira = }')
print()

atribuicao_modulo = 30
atribuicao_modulo %= 2 # 30 mod 2 = 0 (retorna sempre o resto)
print(f'{atribuicao_modulo = }')
print()